// BRINEIDY SALOMÓN precedencia

// La precedencia sirve para definir en que orden se van a ejecutar las operaciones aritméticas
// Ejemplo :

let result;

result = 20 + 30 * 2;              // resultado = 80
result = 40 / 2 + 30 - 20 * 2;     // resultado = 10
result = 40 / 2 * (30 - 20) * 2;   // resultado = 400
result = 12 ** 2 + 100 / 4 + 10 * 4; // resultado = 144 + 25 + 40 = 209

console.log(result);